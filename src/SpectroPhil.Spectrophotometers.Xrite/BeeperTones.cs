namespace SpectroPhil.Spectrophotometers.Xrite
{
    /// <summary>
    /// Enumerates beeper tones.
    /// </summary>
    public enum BeeperTones
    {
        /// <summary>
        /// Beeper tones are off.
        /// </summary>
        Off = 0,

        /// <summary>
        /// Soft beeper tones.
        /// </summary>
        Soft = 1,

        /// <summary>
        /// Medium beeper tones.
        /// </summary>
        Medium = 2,

        /// <summary>
        /// Loud beeper tones.
        /// </summary>
        Loud = 3,
    }
}